import React, {useState} from "react";
import {motion} from "framer-motion";
import {Moon, Sun, X, Menu} from "lucide-react";

const Navbar = ({ darkMode, toggleDarkMode }) => {
    const [activeSection, setActiveSection] = useState('home');
    const [isMenuOpen, setIsMenuOpen] = useState(false);

    const navItems = [
        {name: 'Home', link: '#home'},
        {name: 'About', link: '#about'},
        {name: 'Skills', link: '#skills'},
        {name: 'Projects', link: '#projects'},
        {name: 'Contact', link: '#contact'},
    ];

    const handleNavClick = (itemName) => {
        setActiveSection(itemName.toLowerCase());
        setIsMenuOpen(false);
    };
        return (
        <div className="flex justify-center w-full fixed z-50 mt-4">
            <motion.nav
            initial={{y: -100}}
            animate={{y: 0}}
            transition={{duration: 0.5}}
            className="flex items-center justify-center
                       bg-gradient-to-br from-orange-200 to-white
                       dark:from-gray-700 dark:to-black
                       backdrop-blur-lg rounded-2xl px-4 lg:px-8 py-2 shadow-lg"
            >
                <div className="flex items-center justify-between w-full space-x-6 lg:space-x-8">
                    {/* logo */}
                    <motion.a
                        href="/"
                        whileHover={{scale: 1.05}}
                        className="flex items-center space-x-2">

                        <span className="text-xl font-bold text-gray-900 dark:text-white">
                            Portfolio<span className="text-orange-500">.</span>
                        </span>
                    </motion.a>

                    {/* Navigation Items */}
                    <div className="hidden lg:flex items-center space-x-6">
                        {navItems.map((item) => (
                            <a
                            key={item.name}
                            href={item.link}
                            onClick={() => handleNavClick(item.name)}
                            className="relative">
                                <motion.span
                                    className={`font-medium transition-colors duration-300
                                                ${activeSection === item.name.toLowerCase()
                                                    ? "text-orange-600 dark:text-orange-400"
                                                    : "text-gray-800 dark:text-gray-300 hover:text-orange-500 dark:hover:text-orange-400"
                                    }`}
                                    whileHover={{scale: 1.05}}
                                    whileTap={{scale: 0.95}}>
                                    {item.name}
                                </motion.span>
                                {activeSection === item.name.toLowerCase() && (
                                    <motion.div
                                    layoutId="navbar-indicator"
                                    className="absolute -bottom-1 left-0 right-0 h-0.5
                                               bg-gradient-to-r from-orange-500 to-amber-500
                                               rounded-full"
                                    >
                                    </motion.div>
                                )}
                            </a>
                        ))}
                    </div>
                    <div className="flex items-center space-x-2">

                        {/* DarkMode Toggle */}
                        <motion.button
                        whileHover={{scale: 1.1}}
                        whileTap={{scale: 0.9}}
                        onClick={toggleDarkMode}
                        className="p-2 rounded-full
                                   bg-gray-200 dark:bg-gray-700
                                   transition-colors"
                        aria-label={darkMode
                        ? 'Switch to light mode'
                        : 'Switch to dark mode'
                        }>
                            {darkMode ? (
                                <Sun className="w-5 h-5 text-yellow-300"/>
                            ) : (
                                <Moon className="w-5 h-5 text-gray-700"/>
                                )}
                        </motion.button>

                        {/* Button */}
                        <motion.a
                            href="#contact"
                            whileHover={{scale: 1.05}}
                            whileTap={{scale: 0.95}}
                            className="hidden lg:block px-6 py-2 font-semibold rounded-full
                                       bg-gradient-to-r from-orange-500 to-amber-500
                                       text-white shadow-md hover:shadow-lg transition-shadow"
                        >
                            Get in Touch
                        </motion.a>
                    </div>

                    {/* Mobile Menu Button */}
                    <div className="flex lg:hidden items-center space-x-4 px-2">
                        <motion.button
                            whileTap={{ scale: 0.9 }}
                            onClick={() => setIsMenuOpen(!isMenuOpen)}
                            className="p-2 rounded-lg bg-gray-200 dark:bg-gray-700"
                        >
                            {isMenuOpen ? (
                                <X className="w-5 h-5 text-gray-700 dark:text-white"/>
                            ) : (
                                <Menu className="w-5 h-5 text-gray-700 dark:text-white"/>
                            )}
                        </motion.button>
                    </div>
                </div>
                {isMenuOpen && (
                    <motion.div
                        initial={{opacity: 0, height: 0}}
                        animate={{opacity: 1, height: 'auto'}}
                        exit={{opacity: 0, height: 0}}
                        transition={{duration: 0.3}}
                        className="absolute top-full left-0 right-0 mt-2 lg:hidden
                                   bg-white/95 dark:bg-gray-900/95
                                   backdrop-blur-lg rounded-xl shadow-lg border
                                   border-gray-200 dark:border-gray-700"
                    >
                        <div className="px-4 py-3 space-y-2">
                            {navItems.map((item) => (
                                <a
                                    key={item.name}
                                    href={item.link}
                                    onClick={() => handleNavClick(item.name)}
                                    className="block">

                                    <motion.div
                                        whileHover={{x: 5}}
                                        className={`py-3 px-4 rounded-lg text-center
                                        ${activeSection === item.name.toLowerCase()
                                            ? "bg-orange-50 dark:bg-gray-800"
                                            : ""
                                        }`}>
                                        <span
                                            className={`font-medium 
                                                ${activeSection === item.name.toLowerCase()
                                                    ? "text-orange-600 dark:text-orange-400"
                                                    : "text-gray-800 dark:text-gray-300"
                                                }
                                        `}>
                                            {item.name}
                                        </span>
                                    </motion.div>
                                </a>
                            ))}
                            <motion.a
                                href="#contact"
                                onClick={() => setIsMenuOpen(false)}
                                whileTap={{scale: 0.95}}
                                className="block py-3 px-4 text-center font-semibold rounded-lg
                                           bg-gradient-to-r from-orange-500 to-amber-500
                                           text-white shadow-md"
                            >
                                Get in Touch
                            </motion.a>
                        </div>
                    </motion.div>
                )}
            </motion.nav>
        </div>
        )
}

export default Navbar